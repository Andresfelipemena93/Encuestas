<p>Atención de requerimientos de autoridades y suministrar información a entidades administrativas, judiciales y públicas autorizadas por la Ley.</p>
<p>Recaudar la información financiera de los aliados, para aquellos casos en que la campaña tenga alguna relación.</p>
<p>El envío de correspondencia, correos electrónicos, newsletters, contacto telefónico o cualquier otro método con ciudadanos 
